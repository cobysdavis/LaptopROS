class RobotPose:
  "Pose of the superscout"
  
  def __init__(self, x=0.0, y=0.0, t=0.0) :
    self.x = x
    self.y = y
    self.t = t

  def __str__(self) :
    return "RobotPose x: " + str(self.x) + " y: " + str(self.y) + " t: " + str(self.t) 

  def setLocation(other) :
    self.x = other.x
    self.y = other.y
    self.t = other.t

  def setLocation(x, y, t) :
    self.x = x
    self.y = y
    self.t = t
